fun main() {
    // +
    // -
    // *
    // /
    // %

    val x: Int = 3
    val y: Int = 2

    println(x + y)
    println(x - y)
    println(x * y)

    val xAsDouble: Double = x.toDouble()
    val yAsDouble: Double = y.toDouble()

    val res: Double = xAsDouble / yAsDouble

    println(res)

    println(12 % 10)
    println(25 % 10)


    val result: Int = (3 - 2) * 2 / 1
    println(result)

}